def _require_mapping(value: Any, name: str) -> dict[str, Any]:
    if not isinstance(value, dict):
        raise TypeError(f"{name} must be a mapping, got {type(value).__name__}")
    return value


def _require_list(value: Any, name: str) -> list[Any]:
    if not isinstance(value, list):
        raise TypeError(f"{name} must be a list, got {type(value).__name__}")
    return value


def _require_string(value: Any, name: str) -> str:
    if not isinstance(value, str) or not value:
        raise TypeError(f"{name} must be a non-empty string")
    return value


def _require_int(value: Any, name: str) -> int:
    if isinstance(value, bool) or not isinstance(value, int):
        raise TypeError(f"{name} must be an integer")
    return int(value)


def _require_number(value: Any, name: str) -> float:
    if isinstance(value, bool) or not isinstance(value, (int, float)):
        raise TypeError(f"{name} must be numeric")
    return float(value)


def _parse_object_ids(raw_value: str) -> list[str]:
    object_ids = [item.strip() for item in raw_value.split(",")]
    if not object_ids or any(not item for item in object_ids):
        raise ValueError("--object-ids must be a comma-separated list of non-empty IDs")
    if len(object_ids) != len(set(object_ids)):
        raise ValueError("--object-ids contains duplicate IDs")
    return object_ids


def _resolve_object_ids(config, object_ids_arg: str | None) -> list[str]:
    available_object_ids = config.list_object_ids()
    if object_ids_arg is None:
        return available_object_ids

    requested_object_ids = _parse_object_ids(object_ids_arg)
    available_set = set(available_object_ids)
    unknown_or_filtered = [
        object_id for object_id in requested_object_ids if object_id not in available_set
    ]
    if unknown_or_filtered:
        raise ValueError(
            "Unknown or filtered-out object IDs in --object-ids: "
            + ", ".join(unknown_or_filtered)
        )
    return requested_object_ids


def _validate_matrix4x4(value: Any, name: str) -> np.ndarray:
    rows = _require_list(value, name)
    if len(rows) != 4:
        raise ValueError(f"{name} must contain 4 rows, got {len(rows)}")

    matrix = np.zeros((4, 4), dtype=np.float64)
    for row_idx, row in enumerate(rows):
        row_values = _require_list(row, f"{name}[{row_idx}]")
        if len(row_values) != 4:
            raise ValueError(f"{name}[{row_idx}] must contain 4 values, got {len(row_values)}")
        for col_idx, item in enumerate(row_values):
            matrix[row_idx, col_idx] = _require_number(item, f"{name}[{row_idx}][{col_idx}]")
    return matrix


def _load_json(path: Path, name: str) -> dict[str, Any]:
    if not path.is_file():
        raise FileNotFoundError(f"Missing {name}: {path}")
    with path.open("r", encoding="utf-8") as handle:
        return _require_mapping(json.load(handle), f"{name}[{path}]")


def load_part_specs(part_info_path: Path, object_id: str) -> list[PartSpec]:
    part_info = _load_json(part_info_path, "part_info")
    loaded_object_id = _require_string(part_info.get("object_id"), "part_info['object_id']")
    if loaded_object_id != object_id:
        raise ValueError(
            f"part_info object_id mismatch: expected {object_id}, got {loaded_object_id}"
        )

    num_parts = _require_int(part_info.get("num_parts"), "part_info['num_parts']")
    if num_parts < 1:
        raise ValueError(f"part_info['num_parts'] must be >= 1, got {num_parts}")

    label_to_key = _require_mapping(part_info.get("label_to_key"), "part_info['label_to_key']")
    parts = _require_mapping(part_info.get("parts"), "part_info['parts']")
    if len(label_to_key) != num_parts:
        raise ValueError(
            f"part_info['label_to_key'] size mismatch: expected {num_parts}, got {len(label_to_key)}"
        )
    if len(parts) != num_parts:
        raise ValueError(
            f"part_info['parts'] size mismatch: expected {num_parts}, got {len(parts)}"
        )

    specs: list[PartSpec] = []
    seen_names: set[str] = set()
    for part_index in range(num_parts):
        index_key = str(part_index)
        if index_key not in label_to_key:
            raise KeyError(f"part_info['label_to_key'] missing key '{index_key}'")

        canonical_name = _require_string(
            label_to_key[index_key],
            f"part_info['label_to_key']['{index_key}']",
        )
        if canonical_name in seen_names:
            raise ValueError(f"Duplicate canonical name in part_info: {canonical_name}")
        seen_names.add(canonical_name)

        if canonical_name not in parts:
            raise KeyError(f"Canonical key '{canonical_name}' missing from part_info['parts']")
        part_entry = _require_mapping(
            parts[canonical_name],
            f"part_info['parts']['{canonical_name}']",
        )

        loaded_part_index = _require_int(
            part_entry.get("part_index"),
            f"part_info['parts']['{canonical_name}']['part_index']",
        )
        if loaded_part_index != part_index:
            raise ValueError(
                f"part_info part_index mismatch for '{canonical_name}': "
                f"expected {part_index}, got {loaded_part_index}"
            )

        label = _require_int(
            part_entry.get("label"),
            f"part_info['parts']['{canonical_name}']['label']",
        )
        expected_label = part_index + 1
        if label != expected_label:
            raise ValueError(
                f"part_info label mismatch for '{canonical_name}': expected {expected_label}, got {label}"
            )

        raw_obj_files = _require_list(
            part_entry.get("obj_files"),
            f"part_info['parts']['{canonical_name}']['obj_files']",
        )
        if not raw_obj_files:
            raise ValueError(f"part '{canonical_name}' must reference at least one OBJ file")

        obj_files = tuple(
            _require_string(
                obj_name,
                f"part_info['parts']['{canonical_name}']['obj_files'][{obj_idx}]",
            )
            for obj_idx, obj_name in enumerate(raw_obj_files)
        )

        specs.append(
            PartSpec(
                canonical_name=canonical_name,
                part_index=part_index,
                label=label,
                obj_files=obj_files,
            )
        )

    return specs


def load_joint_archive(
    joint_transforms_path: Path,
    object_id: str,
    expected_num_parts: int,
    expected_num_angles: int,
) -> dict[str, Any]:
    archive = _load_json(joint_transforms_path, "joint_transforms")

    loaded_object_id = _require_string(archive.get("object_id"), "joint_transforms['object_id']")
    if loaded_object_id != object_id:
        raise ValueError(
            f"joint_transforms object_id mismatch: expected {object_id}, got {loaded_object_id}"
        )

    num_parts = _require_int(archive.get("num_parts"), "joint_transforms['num_parts']")
    if num_parts != expected_num_parts:
        raise ValueError(
            f"joint_transforms num_parts mismatch: expected {expected_num_parts}, got {num_parts}"
        )

    angles = _require_mapping(archive.get("angles"), "joint_transforms['angles']")
    expected_angle_keys = {str(angle_idx) for angle_idx in range(expected_num_angles)}
    actual_angle_keys = set(angles)
    missing_angles = sorted(expected_angle_keys - actual_angle_keys)
    extra_angles = sorted(actual_angle_keys - expected_angle_keys)
    if missing_angles:
        raise KeyError(f"Missing angles in {joint_transforms_path}: {missing_angles}")
    if extra_angles:
        raise KeyError(f"Unexpected angles in {joint_transforms_path}: {extra_angles}")

    return archive


def extract_part_transforms(
    joint_archive: dict[str, Any],
    angle_idx: int,
    expected_num_parts: int,
) -> dict[int, np.ndarray]:
    angles = _require_mapping(joint_archive["angles"], "joint_transforms['angles']")
    angle_key = str(angle_idx)
    if angle_key not in angles:
        raise KeyError(f"Missing angle '{angle_key}' in joint_transforms")
    angle_data = _require_mapping(
        angles[angle_key],
        f"joint_transforms['angles']['{angle_key}']",
    )
    _require_mapping(
        angle_data.get("joint_states"),
        f"joint_transforms['angles']['{angle_key}']['joint_states']",
    )
    part_transforms = _require_mapping(
        angle_data.get("part_transforms"),
        f"joint_transforms['angles']['{angle_key}']['part_transforms']",
    )

    expected_keys = {str(part_idx) for part_idx in range(expected_num_parts)}
    actual_keys = set(part_transforms)
    missing_keys = sorted(expected_keys - actual_keys)
    extra_keys = sorted(actual_keys - expected_keys)
    if missing_keys:
        raise KeyError(f"Missing part transforms for angle_{angle_idx}: {missing_keys}")
    if extra_keys:
        raise KeyError(f"Unexpected part transforms for angle_{angle_idx}: {extra_keys}")

    return {
        part_idx: _validate_matrix4x4(
            part_transforms[str(part_idx)],
            f"part_transforms['{part_idx}']",
        )
        for part_idx in range(expected_num_parts)
    }


def load_obj_geometry_fast(path: Path) -> trimesh.Trimesh:
    vertices: list[tuple[float, float, float]] = []
    faces: list[list[int]] = []

    with path.open("r", encoding="utf-8", errors="ignore") as handle:
        for line in handle:
            if line.startswith("v "):
                x, y, z = line.split()[1:4]
                vertices.append((float(x), float(y), float(z)))
            elif line.startswith("f "):
                face = [int(token.split("/")[0]) - 1 for token in line.split()[1:]]
                faces.append(face)

    vertices_array = np.asarray(vertices, dtype=np.float64)
    if not faces:
        faces_array = np.empty((0, 3), dtype=np.int64)
    else:
        face_sizes = {len(face) for face in faces}
        if len(face_sizes) != 1:
            raise ValueError(f"Mixed face sizes are not supported in {path}")
        face_size = next(iter(face_sizes))
        if face_size not in (3, 4):
            raise ValueError(f"Unsupported face size {face_size} in {path}")
        faces_array = np.asarray(faces, dtype=np.int64)

    return trimesh.Trimesh(vertices_array, faces_array, process=False)


def load_part_meshes(part_specs: list[PartSpec], obj_dir: Path) -> dict[int, trimesh.Trimesh]:
    if not obj_dir.is_dir():
        raise FileNotFoundError(f"Missing OBJ directory: {obj_dir}")

    part_meshes: dict[int, trimesh.Trimesh] = {}
    for spec in part_specs:
        meshes: list[trimesh.Trimesh] = []
        for obj_name in spec.obj_files:
            obj_path = obj_dir / f"{obj_name}.obj"
            if not obj_path.is_file():
                raise FileNotFoundError(
                    f"Missing OBJ for part '{spec.canonical_name}': {obj_path}"
                )
            meshes.append(load_obj_geometry_fast(obj_path))

        combined = trimesh.util.concatenate(meshes)
        if len(combined.vertices) == 0:
            raise ValueError(f"Empty combined mesh for part '{spec.canonical_name}'")
        part_meshes[spec.part_index] = combined

    return part_meshes


def load_canonical_normalization(normalization_path: Path) -> CanonicalNormalization:
    payload = _load_json(normalization_path, "canonical normalization")

    scale = _require_number(payload.get("scale"), "canonical_normalization['scale']")
    if not np.isfinite(scale) or scale <= 0.0:
        raise ValueError(f"canonical_normalization['scale'] must be positive finite, got {scale}")

    raw_offset = _require_list(payload.get("offset"), "canonical_normalization['offset']")
    if len(raw_offset) != 3:
        raise ValueError(
            f"canonical_normalization['offset'] must contain 3 values, got {len(raw_offset)}"
        )
    offset = np.asarray(
        [
            _require_number(raw_offset[index], f"canonical_normalization['offset'][{index}]")
            for index in range(3)
        ],
        dtype=np.float64,
    )
    if not np.isfinite(offset).all():
        raise ValueError(
            f"canonical_normalization['offset'] contains non-finite values: {offset.tolist()}"
        )

    raw_aabb = _require_list(payload.get("aabb"), "canonical_normalization['aabb']")
    if len(raw_aabb) != 2:
        raise ValueError(f"canonical_normalization['aabb'] must contain 2 rows, got {len(raw_aabb)}")
    aabb = np.zeros((2, 3), dtype=np.float64)
    for row_index, raw_row in enumerate(raw_aabb):
        row = _require_list(raw_row, f"canonical_normalization['aabb'][{row_index}]")
        if len(row) != 3:
            raise ValueError(
                f"canonical_normalization['aabb'][{row_index}] must contain 3 values, got {len(row)}"
            )
        for col_index, value in enumerate(row):
            aabb[row_index, col_index] = _require_number(
                value,
                f"canonical_normalization['aabb'][{row_index}][{col_index}]",
            )
    expected_aabb = np.asarray([[-0.5, -0.5, -0.5], [0.5, 0.5, 0.5]], dtype=np.float64)
    if not np.allclose(aabb, expected_aabb, rtol=0.0, atol=1e-12):
        raise ValueError(
            "canonical_normalization['aabb'] must equal "
            f"{expected_aabb.tolist()}, got {aabb.tolist()}"
        )

    return CanonicalNormalization(scale=scale, offset=offset)


