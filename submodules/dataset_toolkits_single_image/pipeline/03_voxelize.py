# Phase 1 (B++ palette redesign):
# Dataset can hold up to ~120 parts per object (e.g. 100452 has 2; large
# articulated objects approach 120). Original 10-color list with `% 10` modulo
# meant labels 1/11/21/... shared the same color — visually unusable for
# >10-part objects. The redesign:
# - BASE_BODY_COLOR is neutral gray so it never collides with any PART_COLORS hue.
# - PART_COLORS is generated by golden-ratio HSV sampling (138° hue step) with
#   3 (saturation, value) variants cycling every 3 entries — so even when the
#   golden-ratio cycle revisits a similar hue, the new entry has different
#   brightness/saturation and stays distinguishable.
# - 128 colors total: any consecutive label pair has RGB-Euclidean distance ≥ 0.90
#   (well above the ~0.30 threshold for human discriminability), so a 120-part
#   object's neighbors stay visually distinct.
BASE_BODY_COLOR = (0.45, 0.45, 0.45, 0.98)


def _build_part_palette(n: int = 128) -> list[tuple[float, float, float, float]]:
    """Golden-ratio HSV palette: max perceptual distance between consecutive labels."""
    GOLDEN = 0.381966011  # 1 - 1/phi
    sv_variants = [(0.85, 0.85), (0.62, 0.95), (0.95, 0.65)]
    palette: list[tuple[float, float, float, float]] = []
    for idx in range(n):
        hue = (idx * GOLDEN) % 1.0
        sat, val = sv_variants[idx % len(sv_variants)]
        r, g, b = colorsys.hsv_to_rgb(hue, sat, val)
        palette.append((r, g, b, 0.98))
    return palette


