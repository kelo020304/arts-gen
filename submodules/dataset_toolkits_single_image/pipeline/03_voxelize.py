def convert_part_transform_to_blender_space(raw_matrix: np.ndarray, obj_up_axis: str) -> np.ndarray:
    obj_up_axis = obj_up_axis.upper()
    if obj_up_axis == "Y":
        return Y_UP_TO_Z_UP @ raw_matrix
    if obj_up_axis == "Z":
        return raw_matrix
    raise ValueError(f"obj_up_axis must be 'Y' or 'Z', got {obj_up_axis!r}")


def movtran(mesh: trimesh.Trimesh, offset: np.ndarray, scale: float) -> trimesh.Trimesh:
    mesh.apply_transform(trimesh.transformations.scale_matrix(scale))
    mesh.apply_translation([float(offset[0]), float(offset[1]), float(offset[2])])
    return mesh


def sanitize_trimesh_for_voxelization(mesh: trimesh.Trimesh) -> trimesh.Trimesh:
    vertices = np.asarray(mesh.vertices, dtype=np.float64)
    faces = np.asarray(mesh.faces, dtype=np.int64)

    if vertices.ndim != 2 or vertices.shape[1] != 3 or len(vertices) == 0:
        raise ValueError("Mesh has empty vertices")
    if faces.ndim != 2 or len(faces) == 0:
        raise ValueError("Mesh has empty faces")
    if not np.isfinite(vertices).all():
        raise ValueError("Mesh has non-finite vertices")
    if not np.isfinite(faces).all():
        raise ValueError("Mesh has non-finite faces")

    if faces.shape[1] == 4:
        faces = trimesh.geometry.triangulate_quads(faces)
    elif faces.shape[1] != 3:
        raise ValueError(f"Unsupported face size {faces.shape[1]}")

    valid_face_mask = np.all((faces >= 0) & (faces < len(vertices)), axis=1)
    faces = faces[valid_face_mask]
    if len(faces) == 0:
        raise ValueError("Mesh has no valid faces")

    tri_vertices = vertices[faces]
    tri_areas = np.linalg.norm(
        np.cross(
            tri_vertices[:, 1] - tri_vertices[:, 0],
            tri_vertices[:, 2] - tri_vertices[:, 0],
        ),
        axis=1,
    ) * 0.5
    valid_area_mask = np.isfinite(tri_areas) & (tri_areas > 1e-12)
    faces = faces[valid_area_mask]
    if len(faces) == 0:
        raise ValueError("All mesh faces are degenerate")

    valid_vertex_ids = np.unique(faces.reshape(-1))
    remap = np.full(len(vertices), -1, dtype=np.int64)
    remap[valid_vertex_ids] = np.arange(len(valid_vertex_ids), dtype=np.int64)
    compact_vertices = vertices[valid_vertex_ids]
    compact_faces = remap[faces]

    bbox_extent = np.ptp(compact_vertices, axis=0)
    if not np.isfinite(bbox_extent).all():
        raise ValueError("Mesh has non-finite bbox extent")
    if np.max(bbox_extent) <= 1e-12:
        raise ValueError("Mesh has zero bbox extent")

    return trimesh.Trimesh(vertices=compact_vertices, faces=compact_faces, process=False)


def trimesh_to_o3d_mesh(mesh: trimesh.Trimesh) -> o3d.geometry.TriangleMesh:
    o3d_mesh = o3d.geometry.TriangleMesh()
    o3d_mesh.vertices = o3d.utility.Vector3dVector(
        np.clip(np.asarray(mesh.vertices, dtype=np.float64), -0.5 + 1e-6, 0.5 - 1e-6)
    )
    o3d_mesh.triangles = o3d.utility.Vector3iVector(np.asarray(mesh.faces, dtype=np.int32))
    return o3d_mesh


def voxelize_mesh_surface_open3d(mesh: trimesh.Trimesh, resolution: int) -> np.ndarray:
    """Per-part surface voxelization via Open3D create_from_triangle_mesh_within_bounds.

    Mirrors OmniPart voxelize_part.py exactly: clamp vertices to [-0.5+1e-6, 0.5-1e-6],
    construct VoxelGrid in [-0.5, 0.5]^3 with voxel_size=1/resolution, return integer
    grid_indices as (N, 3) int64. No surface sampling, no flood fill.
    """
    clean_mesh = sanitize_trimesh_for_voxelization(mesh)
    o3d_mesh = trimesh_to_o3d_mesh(clean_mesh)
    voxel_size = 1.0 / float(resolution)
    voxel_grid = o3d.geometry.VoxelGrid.create_from_triangle_mesh_within_bounds(
        o3d_mesh,
        voxel_size=voxel_size,
        min_bound=(-0.5, -0.5, -0.5),
        max_bound=(0.5, 0.5, 0.5),
    )
    voxels = voxel_grid.get_voxels()
    if len(voxels) == 0:
        return np.empty((0, 3), dtype=np.int64)
    indices = np.asarray([v.grid_index for v in voxels], dtype=np.int64)
    if np.any(indices < 0) or np.any(indices >= resolution):
        bad = indices[np.any((indices < 0) | (indices >= resolution), axis=1)][0]
        raise ValueError(
            f"voxelize_mesh_surface_open3d produced out-of-range index {bad.tolist()} "
            f"for resolution {resolution}"
        )
    return np.unique(indices, axis=0)


def setup_voxel_axes(
    ax: Any,
    object_id: str,
    angle_idx: int,
    resolution: int,
    title: str,
) -> None:
    ax.set_title(f"{object_id}/angle_{angle_idx} - {title}", fontsize=11)
    ax.set_xlabel("X")
    ax.set_ylabel("Y")
    ax.set_zlabel("Z")
    ax.set_xlim(0, resolution)
    ax.set_ylim(0, resolution)
    ax.set_zlim(0, resolution)
    ax.set_box_aspect((1, 1, 1))
    # Match Blender view_1 camera (azim=45, elev=30 in scene world).
    # Meshes are already converted to Blender Z-up before movtran; movtran only
    # applies scale and translation, so voxel indices keep the Blender XYZ axes.
    # Blender camera direction:
    #   (cos(-45)*cos(30), sin(-45)*cos(30), sin(30))
    #   = (0.612, -0.612, 0.5)
    # Matplotlib uses the same spherical convention for the eye position:
    #   elev = atan2(z, sqrt(x^2+y^2)) = 30°
    #   azim = atan2(y, x) = -45°
    ax.view_init(elev=VIEW1_ELEV, azim=VIEW1_AZIM)


def require_voxel_indices(indices: np.ndarray, resolution: int, name: str) -> np.ndarray:
    indices = np.asarray(indices, dtype=np.int64)
    if indices.ndim != 2 or indices.shape[1] != 3:
        raise ValueError(f"{name} must have shape N x 3, got {indices.shape}")
    if len(indices) == 0:
        return indices
    if np.any(indices < 0) or np.any(indices >= resolution):
        bad = indices[np.any((indices < 0) | (indices >= resolution), axis=1)][0]
        raise ValueError(
            f"{name} contains out-of-range voxel coordinate {bad.tolist()} "
            f"for resolution {resolution}"
        )
    return np.unique(indices, axis=0)


def indices_to_filled_volume(indices: np.ndarray, resolution: int, name: str) -> np.ndarray:
    checked = require_voxel_indices(indices, resolution, name)
    filled = np.zeros((resolution, resolution, resolution), dtype=bool)
    if len(checked) > 0:
        filled[checked[:, 0], checked[:, 1], checked[:, 2]] = True
    return filled


