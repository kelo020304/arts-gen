def is_focus_part_name(part_name: str) -> bool:
    lower_name = part_name.lower()
    return any(keyword in lower_name for keyword in FOCUS_PART_KEYWORDS)


def is_base_part_name(part_name: str) -> bool:
    lower_name = part_name.lower()
    return lower_name.startswith("base_") or "base" in lower_name or "body" in lower_name


def unique_valid_indices(indices: np.ndarray, resolution: int) -> np.ndarray:
    return require_voxel_indices(indices, resolution, "part indices")


def encode_indices(indices: np.ndarray, resolution: int) -> np.ndarray:
    indices = unique_valid_indices(indices, resolution)
    if len(indices) == 0:
        return np.empty((0,), dtype=np.int64)
    return (
        indices[:, 0] * resolution * resolution
        + indices[:, 1] * resolution
        + indices[:, 2]
    ).astype(np.int64)


def decode_indices(encoded: np.ndarray, resolution: int) -> np.ndarray:
    encoded = np.asarray(encoded, dtype=np.int64)
    if encoded.ndim != 1 or len(encoded) == 0:
        return np.empty((0, 3), dtype=np.int64)
    x = encoded // (resolution * resolution)
    rem = encoded % (resolution * resolution)
    y = rem // resolution
    z = rem % resolution
    return np.column_stack((x, y, z)).astype(np.int64)


def subtract_indices(indices: np.ndarray, remove_indices: np.ndarray, resolution: int) -> np.ndarray:
    indices = unique_valid_indices(indices, resolution)
    remove_encoded = encode_indices(remove_indices, resolution)
    if len(indices) == 0 or len(remove_encoded) == 0:
        return indices
    keep_mask = ~np.isin(encode_indices(indices, resolution), remove_encoded)
    return indices[keep_mask]


def collect_part_union(
    part_specs: list[PartSpec],
    per_part_indices: dict[str, np.ndarray],
    resolution: int,
    predicate,
) -> np.ndarray:
    encoded_parts: list[np.ndarray] = []
    for spec in part_specs:
        if not predicate(spec.canonical_name):
            continue
        encoded = encode_indices(per_part_indices[spec.canonical_name], resolution)
        if len(encoded) > 0:
            encoded_parts.append(encoded)
    if not encoded_parts:
        return np.empty((0, 3), dtype=np.int64)
    return decode_indices(np.unique(np.concatenate(encoded_parts)), resolution)


def render_per_part_voxel(
    part_specs: list[PartSpec],
    per_part_indices: dict[str, np.ndarray],
    out_path: Path,
    object_id: str,
    angle_idx: int,
    resolution: int,
) -> None:
    fig = plt.figure(figsize=(10, 10), dpi=100)
    ax = fig.add_subplot(111, projection="3d")
    setup_voxel_axes(ax, object_id, angle_idx, resolution, "per-part colors")

    legend_handles: list[Patch] = []
    indexed_parts: list[tuple[PartSpec, np.ndarray]] = []
    for spec in part_specs:
        raw = per_part_indices.get(spec.canonical_name)
        if raw is None or len(raw) <= MIN_PART_VOXELS:
            continue
        indices = unique_valid_indices(raw, resolution)
        if len(indices) == 0:
            continue
        indexed_parts.append((spec, indices))

    focus_union = collect_part_union(
        part_specs,
        per_part_indices,
        resolution,
        is_focus_part_name,
    )
    label_volume = np.zeros((resolution, resolution, resolution), dtype=np.int64)
    color_volume = np.zeros((resolution, resolution, resolution, 4), dtype=np.float64)

    def display_order(item: tuple[PartSpec, np.ndarray]) -> tuple[int, int, int]:
        spec, indices = item
        if is_base_part_name(spec.canonical_name):
            layer = 0
        elif is_focus_part_name(spec.canonical_name):
            layer = 2
        else:
            layer = 1
        return (layer, -len(indices), spec.label)

    for spec, indices in sorted(indexed_parts, key=display_order):
        if is_base_part_name(spec.canonical_name):
            indices = subtract_indices(indices, focus_union, resolution)
            if len(indices) == 0:
                continue

        if is_base_part_name(spec.canonical_name):
            display_color = np.asarray(BASE_BODY_COLOR, dtype=np.float64).copy()
        else:
            display_color = np.asarray(PART_COLORS[(spec.label - 1) % len(PART_COLORS)], dtype=np.float64).copy()
        display_color[3] = 0.98

        clipped = unique_valid_indices(indices, resolution)
        label_volume[clipped[:, 0], clipped[:, 1], clipped[:, 2]] = spec.label
        color_volume[clipped[:, 0], clipped[:, 1], clipped[:, 2]] = display_color
        legend_handles.append(
            Patch(
                facecolor=display_color[:3],
                edgecolor="none",
                alpha=display_color[3],
                label=spec.canonical_name,
            )
        )

    filled = label_volume > 0
    ax.voxels(
        filled,
        facecolors=color_volume,
        edgecolors=(0.04, 0.04, 0.04, 0.22),
        linewidth=0.12,
    )

    if legend_handles:
        ax.legend(handles=legend_handles, loc="upper left", fontsize=9, framealpha=0.8)

    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.subplots_adjust(left=0.04, right=0.96, bottom=0.08, top=0.94)
    fig.savefig(out_path)
    plt.close(fig)


def process_object(work_item: WorkItem) -> dict[str, Any]:
    object_id = work_item.object_id
    data_root = Path(work_item.data_root)
    raw_dir = data_root / "raw"
    renders_dir = data_root / "renders"
    reconstruction_dir = data_root / "reconstruction"
    joint_transforms_path = data_root / "joint_transforms" / f"{object_id}.json"

    result: dict[str, Any] = {
        "object_id": object_id,
        "processed_angles": 0,
        "skipped_angles": 0,
        "errors": [],
    }

    try:
        part_specs = load_part_specs(
            data_root / "part_info" / object_id / "part_info.json",
            object_id,
        )
        joint_archive = load_joint_archive(
            joint_transforms_path,
            object_id,
            expected_num_parts=len(part_specs),
            expected_num_angles=work_item.num_angles,
        )
        base_part_meshes = load_part_meshes(part_specs, raw_dir / "partseg" / object_id / "objs")
    except Exception as exc:
        result["errors"].append(f"setup: {exc}")
        result["status"] = "error"
        return result

    voxel_root = reconstruction_dir / "voxel_expanded" / object_id

    for angle_idx in range(work_item.num_angles):
        voxel_dir = voxel_root / f"angle_{angle_idx}" / str(work_item.resolution)
        surface_path = voxel_dir / "surface.npy"
        if surface_path.is_file():
            result["skipped_angles"] += 1
            continue

        try:
            part_transforms = extract_part_transforms(
                joint_archive,
                angle_idx=angle_idx,
                expected_num_parts=len(part_specs),
            )

            transformed_part_meshes: dict[int, trimesh.Trimesh] = {}
            for spec in part_specs:
                mesh = base_part_meshes[spec.part_index].copy()
                mesh.apply_transform(
                    convert_part_transform_to_blender_space(
                        part_transforms[spec.part_index],
                        work_item.obj_up_axis,
                    )
                )
                transformed_part_meshes[spec.part_index] = mesh

            normalization_path = resolve_canonical_normalization_path(
                data_root,
                renders_dir,
                object_id,
                angle_idx,
            )
            normalization = load_canonical_normalization(normalization_path)
            scale = normalization.scale
            offset = normalization.offset
            print(
                "[INFO] canonical normalization "
                f"{object_id}/angle_{angle_idx}: source={normalization_path} "
                f"scale={scale:.17g}, offset={offset.tolist()}",
                flush=True,
            )

            normalized_part_meshes: dict[int, trimesh.Trimesh] = {}
            for spec in part_specs:
                normalized_mesh = transformed_part_meshes[spec.part_index].copy()
                movtran(normalized_mesh, offset, scale)
                normalized_part_meshes[spec.part_index] = normalized_mesh

            # Per-part surface voxelization via Open3D
            # create_from_triangle_mesh_within_bounds (OmniPart parity).
            per_part_indices: dict[str, np.ndarray] = {}
            for spec in part_specs:
                indices = voxelize_mesh_surface_open3d(
                    normalized_part_meshes[spec.part_index],
                    work_item.resolution,
                )
                per_part_indices[spec.canonical_name] = indices

            # Overall surface = unique(vstack(per-part filtered)). Filter is the same
            # MIN_PART_VOXELS threshold used when persisting per-part files, so the
            # invariant np.array_equal(unique(surface), unique(vstack(persisted)))
            # holds by construction (matches OmniPart voxelize_overall.py).
            valid_part_arrays: list[np.ndarray] = []
            for spec in part_specs:
                ind = per_part_indices[spec.canonical_name]
                if len(ind) <= MIN_PART_VOXELS:
                    continue
                valid_part_arrays.append(ind)

            if not valid_part_arrays:
                raise ValueError(
                    f"angle_{angle_idx}: all parts filtered out by MIN_PART_VOXELS={MIN_PART_VOXELS}"
                )

            surface_indices = np.unique(np.vstack(valid_part_arrays), axis=0)

            voxel_dir.mkdir(parents=True, exist_ok=True)
            for spec in part_specs:
                ind = per_part_indices[spec.canonical_name]
                if len(ind) <= MIN_PART_VOXELS:
                    continue
                np.save(voxel_dir / f"ind_{spec.canonical_name}.npy", ind)

            viz_dir = voxel_dir / "viz"
            render_surface_voxel(
                allind=surface_indices,
                out_path=viz_dir / "surface_voxel.png",
                object_id=object_id,
                angle_idx=angle_idx,
                resolution=work_item.resolution,
            )
            render_per_part_voxel(
                part_specs=part_specs,
                per_part_indices=per_part_indices,
                out_path=viz_dir / "per_part_voxel.png",
                object_id=object_id,
                angle_idx=angle_idx,
                resolution=work_item.resolution,
            )

            np.save(surface_path, surface_indices)
            result["processed_angles"] += 1
        except Exception as exc:
            result["errors"].append(f"angle_{angle_idx}: {exc}")

    if result["errors"]:
        result["status"] = "partial" if result["processed_angles"] or result["skipped_angles"] else "error"
    elif result["processed_angles"] == 0 and result["skipped_angles"] == work_item.num_angles:
        result["status"] = "skipped"
    else:
        result["status"] = "done"
    return result


def build_work_items(config, object_ids: list[str]) -> list[WorkItem]:
    if config.voxel.resolution != EXPECTED_RESOLUTION:
        raise ValueError(
            f"voxel.resolution must be {EXPECTED_RESOLUTION}, got {config.voxel.resolution}"
        )

    return [
        WorkItem(
            data_root=config.data_root,
            object_id=object_id,
            num_angles=config.get_num_angles(object_id),
            resolution=config.voxel.resolution,
            obj_up_axis=resolve_obj_up_axis(
                config,
                Path(config.finaljson_dir) / f"{object_id}.json",
            ),
        )
        for object_id in object_ids
    ]


def format_progress_message(index: int, total: int, result: dict[str, Any]) -> str:
    object_id = result["object_id"]
    status = result["status"]
    processed = result["processed_angles"]
    skipped = result["skipped_angles"]
    errors = len(result["errors"])

    if status == "done":
        tail = f"done ({processed} angles)"
    elif status == "skipped":
        tail = f"skipped ({skipped} existing)"
    elif status == "partial":
        tail = f"partial (processed={processed}, skipped={skipped}, errors={errors})"
    else:
        tail = f"error (processed={processed}, skipped={skipped}, errors={errors})"
    return f"[{index}/{total}] {object_id} {tail}"


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    if args.workers < 1:
        raise ValueError("--workers must be >= 1")

    config = load_config(args.config)
    object_ids = _resolve_object_ids(config, args.object_ids)
    work_items = build_work_items(config, object_ids)

    Path(config.reconstruction_dir, "voxel_expanded").mkdir(parents=True, exist_ok=True)

    print(
        f"[INFO] Voxelizing {len(work_items)} objects "
        f"at {config.voxel.resolution}^3 with {args.workers} worker(s)"
    )

    results: list[dict[str, Any]]
    if args.workers == 1:
        results = []
        for index, item in enumerate(work_items, start=1):
            result = process_object(item)
            results.append(result)
            print(format_progress_message(index, len(work_items), result))
    else:
        results = []
        with Pool(processes=args.workers) as pool:
            for index, result in enumerate(pool.imap_unordered(process_object, work_items), start=1):
                results.append(result)
                print(format_progress_message(index, len(work_items), result))

    done = sum(1 for result in results if result["status"] == "done")
    skipped = sum(1 for result in results if result["status"] == "skipped")
    partial = sum(1 for result in results if result["status"] == "partial")
    error = sum(1 for result in results if result["status"] == "error")
    processed_angles = sum(result["processed_angles"] for result in results)
    skipped_angles = sum(result["skipped_angles"] for result in results)
    error_count = sum(len(result["errors"]) for result in results)

    print("\n[DONE] voxelize.py complete")
    print(f"  Objects done: {done}")
    print(f"  Objects skipped: {skipped}")
    print(f"  Objects partial: {partial}")
    print(f"  Objects error: {error}")
    print(f"  Angles processed: {processed_angles}")
    print(f"  Angles skipped: {skipped_angles}")
    print(f"  Angle/object errors: {error_count}")

    for result in results:
        for error_message in result["errors"]:
            print(f"  [ERROR] {result['object_id']}: {error_message}")

    return 0 if error == 0 and partial == 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
